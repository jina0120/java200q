public class Chapter2_2 {
    public static void main(String[] args) {
        // 2개의 변수 x와 y의 합과 평균구하기
        int x;
        int y;

        x = 63;
        y = 18;
        
        System.out.println("x = " + x);
        System.out.println("y = " + y);
        System.out.println("합 = " + (x + y));
        System.out.println("평균 = " + ((x + y) / 2));
    }
}
