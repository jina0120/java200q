package chapter5;

public class Chapter5_1 {
    public static void main(String[] args) {
        // 8진수 12, 10진수 12, 16진수 12를 각각 10진수로 표시하는 프로그램
        System.out.printf("8진수 12는 10진수로 %d", 012).println();
        System.out.printf("10진수 12는 10진수로 %d", 12).println();
        System.out.printf("16진수 12는 10진수로 %d", 0x12).println();
    }
}
